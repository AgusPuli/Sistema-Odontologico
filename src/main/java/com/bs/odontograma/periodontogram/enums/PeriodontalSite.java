package com.bs.odontograma.periodontogram.enums;

/**
 * 6-site probing per tooth as recommended by AAP and most periodontal literature.
 *   Buccal:  MV (mesiovestibular), V (vestibular center), DV (distovestibular)
 *   Lingual: ML (mesiolingual),    L (lingual center),    DL (distolingual)
 */
public enum PeriodontalSite {
    MV, V, DV, ML, L, DL
}
