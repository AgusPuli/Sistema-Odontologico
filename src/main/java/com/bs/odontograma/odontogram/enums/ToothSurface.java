package com.bs.odontograma.odontogram.enums;

/**
 * Tooth surfaces in standard dental notation.
 *  - M  = Mesial
 *  - D  = Distal
 *  - V  = Vestibular / Buccal
 *  - L  = Lingual / Palatal
 *  - O  = Occlusal (posterior teeth)
 *  - I  = Incisal  (anterior teeth)
 */
public enum ToothSurface {
    M, D, V, L, O, I
}
