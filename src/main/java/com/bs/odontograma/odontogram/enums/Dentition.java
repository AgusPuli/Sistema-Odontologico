package com.bs.odontograma.odontogram.enums;

/**
 * PERMANENT = adult dentition (FDI 11-48, 32 teeth)
 * PRIMARY   = deciduous / pediatric dentition (FDI 51-85, 20 teeth)
 * MIXED     = mixed dentition during pediatric transition; both numbering systems coexist
 */
public enum Dentition {
    PERMANENT,
    PRIMARY,
    MIXED
}
